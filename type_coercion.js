/*
    == compares the two values, it the tow values have different types coerc the
    === compares the values and its type
*/

console.log(`1 == "1": `, 1 == "1");
console.log(`false == "": `, false == "");
console.log(`false == []: `, false == []);
console.log(`false == {}: `, false == {});
console.log(`"" == 0: `, "" == 0);
console.log(`"" == []:`, "" == []);
console.log(`"" == {}:`, "" == {});
console.log(`0 == []:`, 0 == []);
console.log(`0 == {}:`, 0 == {});
console.log(`0 == null:`, 0 == null);
